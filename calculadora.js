var numero1=Number(prompt("Introduzca un número"));
var numero2=Number(prompt("Introduzca otro número"));
var suma=numero1 + numero2;
var diferencia=numero1 - numero2;
var multiplicacion=numero1 * numero2;
var division= numero1 / numero2;

document.write(numero1 + "+" + numero2 + "=" + suma + "<br>");
document.write(numero1 + "-" + numero2 + "=" + diferencia + "<br>");
document.write(numero1 + "*" + numero2 + "=" + multiplicacion + "<br>");
document.write(numero1 + "/" + numero2 + "=" + division + "<br>");
	


